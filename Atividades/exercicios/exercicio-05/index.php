                <p class="card-text">O cavalo (Equus ferus caballus) é uma das duas subespécies existentes de Equus ferus.
                    É um mamífero perissodáctilo pertencente à família taxonômica Equidae. O cavalo evoluiu há entre 45 milhões a 55 milhões de anos, desde uma pequena criatura com vários dedos, o Eohippus, até o animal grande e com um único dedo de hoje. 
